public class MapsActivity extends FragmentActivity implements LocationListener
{
    private static final String GOOGLE_API_KEY = "Your_API_Key";
    GoogleMap googleMap;
    AutoCompleteTextView placeText;
    String [] placesLocate={"hospital","police"};
    double latitude = 0;
    double longitude = 0;
    private int PROXIMITY_RADIUS = 5000;
    private String shared_messages="";

    @Override
    protected void onCreate(Bundle savedInstanceState)
    {
        super.onCreate(savedInstanceState);

         //show error dialog if GoolglePlayServices not available
        if (!isGooglePlayServicesAvailable()) {
            finish();
        }
        setContentView(R.layout.activity_maps);

        placeText = (AutoCompleteTextView) findViewById(R.id.editText1324);
        ArrayAdapter ad=new ArrayAdapter(this,android.R.layout.select_dialog_item,placesLocate);
        placeText.setThreshold(1);
        placeText.setAdapter(ad);

        Button btnFind = (Button) findViewById(R.id.btn_find1234);
        SupportMapFragment fragment = (SupportMapFragment) getSupportFragmentManager().findFragmentById(R.id.map);
        googleMap = fragment.getMap();
        googleMap.setMyLocationEnabled(true);
        LocationManager locationManager = (LocationManager) getSystemService(LOCATION_SERVICE);
        Criteria criteria = new Criteria();
        String bestProvider = locationManager.getBestProvider(criteria, true);
        Location location = locationManager.getLastKnownLocation(bestProvider);
        if (location != null) {
            onLocationChanged(location);
        }
        if (ActivityCompat.checkSelfPermission(this, android.Manifest.permission.ACCESS_FINE_LOCATION) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(this, android.Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        locationManager.requestLocationUpdates(bestProvider, 20000, 0, this);

        btnFind.setOnClickListener(new OnClickListener() {
            @Override
            public void onClick(View v) {
                String type = placeText.getText().toString();
                StringBuilder googlePlacesUrl = new StringBuilder("https://maps.googleapis.com/maps/api/place/nearbysearch/json?");
                googlePlacesUrl.append("location=" + latitude + "," + longitude);
                googlePlacesUrl.append("&radius=" + PROXIMITY_RADIUS);
                googlePlacesUrl.append("&types=" + type);
                googlePlacesUrl.append("&sensor=true");
                googlePlacesUrl.append("&key=" + GOOGLE_API_KEY);

                GooglePlacesReadTask googlePlacesReadTask = new GooglePlacesReadTask();
                Object[] toPass = new Object[2];
                toPass[0] = googleMap;
                toPass[1] = googlePlacesUrl.toString();
                googlePlacesReadTask.execute(toPass);
            }
        });
    }

    private boolean isGooglePlayServicesAvailable() {
        int status = GooglePlayServicesUtil.isGooglePlayServicesAvailable(this);
        if (ConnectionResult.SUCCESS == status) {
            return true;
        } else {
            GooglePlayServicesUtil.getErrorDialog(status, this, 0).show();
            return false;
        }
    }


    @Override
    public void onLocationChanged(Location location) {
        latitude = location.getLatitude();
        longitude = location.getLongitude();
        saveSharedPreferences(latitude,longitude);
        LatLng latLng = new LatLng(latitude, longitude);
        googleMap.moveCamera(CameraUpdateFactory.newLatLng(latLng));
        googleMap.animateCamera(CameraUpdateFactory.zoomTo(12));
    }

    public void saveSharedPreferences(double lati, double longi)
    {
/*
        SharedPreferences sharedPreferences=getSharedPreferences("IMPORT_MESSAGE", Context.MODE_PRIVATE);
        SharedPreferences.Editor editor=sharedPreferences.edit();
        editor.putString("MESSAGE_LIST",""+text);
        editor.apply();
*/

        SharedPreferences preferences = PreferenceManager.getDefaultSharedPreferences(MapsActivity.this);
        SharedPreferences.Editor editor = preferences.edit();
        editor.putString("Latitude", ""+lati);
        editor.putString("Longitude", ""+longi);
        editor.commit();

    }

    public String getSharedPreferences(int i)
    {

     /*   SharedPreferences sharedPreferences=getSharedPreferences("IMPORT_MESSAGE", Context.MODE_PRIVATE);
        shared_messages = sharedPreferences.getString("MESSAGE_LIST","");
     */
     if(i==1) {
         SharedPreferences preferences = PreferenceManager.getDefaultSharedPreferences(MapsActivity.this);
         shared_messages = preferences.getString("Latitude", "null");
     }
     else
     {
         SharedPreferences preferences = PreferenceManager.getDefaultSharedPreferences(MapsActivity.this);
         shared_messages = preferences.getString("Longitude", "null");
     }
        return shared_messages;

    }
    @Override
    public void onProviderDisabled(String provider) {
        // TODO Auto-generated method stub

    }

    @Override
    public void onProviderEnabled(String provider) {
        // TODO Auto-generated method stub

    }

    @Override
    public void onStatusChanged(String provider, int status, Bundle extras) {
        // TODO Auto-generated method stub
    }



}



